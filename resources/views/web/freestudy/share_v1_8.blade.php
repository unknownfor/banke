        那么共产主义就是建立在错误的社会学基础上的（认为存在优等阶级）

        两者虽然就其目的相差甚远（一个试图建立民族帝国，一个试图建立无国家社会），然而在为了达到“最终目的”而不折手段这一点上可以说是完全一致的。

        有很多人错误的认为，纳粹是第一个提出使用人道气体（human gas）去消灭特定人群的反人类组织，然而实际上，早在20世纪初，尚处在“相对温和的”社会主义左派阵营中的George Bernard Shaw就提出要使用人道气体去消灭那些“对社会不再具有贡献的人”，这类人甚至包括残疾人。而在将共产主义革命原则推向极致的前苏联，则更是建立了成千上万的劳改营，来关押大量的政治敌人。

        在几乎所有后来的革命著作中，你都可以看到这样的论调，那就是对阶级敌人的绝不手软，以及阻碍“人民和历史脚步”的人必将被碾碎的宣传话语。这些革命宣传思想的一个潜台词，是所有阻碍无产者达成共产主义社会的人，在必要的时候，都可以牺牲。某些国家的共产主义执政党还特别提出了阶级敌人的借口，来甄别和迫害异见分子。

        早在十八，十九世纪，西方自由主义政治和哲学家就已经提出，要通过保障基本的人权，来建立安稳的社会。然而革命者对于安稳的社会并不满意，在他们看来，为了更好的目的，哪怕去干一些可怕的事情也是情有可原的。而当人们开始接受那种“为了好的目的，去牺牲一些人的性命也在所不惜”的想法时，通往地狱的大门也就敞开了。

        红色高棉不过是这种为了实现人人平等的愿望所导致的其中一个惨绝人寰的悲剧而已。早在越战末期，红色高棉就已经将当时亲美的柬埔寨傀儡当局赶下台，就在民众欢呼红色高棉士兵解放首都的时候，波尔布特领导的激进革命分子却以“金边”可能遭受美国空军袭击，而将全城市民迁移到农村山林地区，开始“共产主义改造”。当然这种激进的改革方式肯定是要闹出人命的，然而在坚信“为了达到好的目的可以不折手段”的红色高棉眼里，这种尝试又何尝不可。很多人认为，是柬埔寨方面的种族屠杀造成了全国近三分之一人口的死亡。然而实际情况却是领导人脑中极不符合实际的激进改革造成大批民众饿死，当然也包括被政治迫害死亡的。
