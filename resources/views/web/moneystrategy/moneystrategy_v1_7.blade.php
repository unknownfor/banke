            　　为产品注入灵魂 ROOBO借设计推动智能机器人行业变革
            　　想要将梦想照进现实，除了需要人工智能在自主学习等领域做出长足进步，遵循有灵魂的设计理念更是整个行业急需正视的要素。可喜的是，在智能机器人涉及的产品、工业设计领域，一家来自国内的年轻厂商为行业做出了表率。
            　　作为面向全球的人工智能解决方案提供商，北京智能管家科技有限公司(ROOBO)品牌旗下智能机器人Jelly robot(以下简称为Jelly)，一举包揽了2017年度iF国际设计奖中关于产品设计的全部奖项(“DISCIPLINE PRODUCT”(产品)、“DISCIPLINE COMMUNICATION”(交互)、“DISCIPLINE PACKAGING”(包装)，这在智能机器人领域中还尚属首例。
            　　为产品注入灵魂 ROOBO借设计推动智能机器人行业变革
            　　Jelly在设计上秉持强调专业化与亲和感的理念。外形方面采用了中性的风格，极简、干净、可靠、优雅的设计形态和产品细节展现出良好的包容性。以语音交互为核心，整合触控、灯光、感应元器件、提供人脸识别等多样式的交互方式，让人机交互回归自然。包装上宛如蚕茧般的一体化设计，不仅与产品本身设计风格保持一致，也成功为用户营造出自始至终的品质感。
            　　以外观架构躯体、系统诠释灵魂，Jelly的设计理念高度统一并始终贯彻每一个细节。给用户一种产品更懂你、细节更贴心、使用更自然的舒适体验，成功让机器人产品剥去了冰冷的外衣。
            　　为产品注入灵魂 ROOBO借设计推动智能机器人行业变革
            　　此外，在刚刚结束的2017年度红点设计奖评选中，ROOBO在今年初发布的新一代智能机器人布丁豆豆(BeanQ)，也成功斩获红点最高荣誉“红点最佳设计奖”(Best of the Best)，这也是全球消费机器人领域中首个也是唯一一个荣获“红点最佳设计奖”的产品。
            　　为产品注入灵魂 ROOBO借设计推动智能机器人行业变革
            　　通过对ROOBO高级设计总监 叶菲梓 先生的采访，我们获悉。面对消费机器人这个崭新品类，BeanQ在设计之初便引发了ROOBO设计团队对于“生命力”这一概念在设计领域的探索和想象。将“机器人”这个词进行拆解，“机器”代表功能层面的诉求，“人”则体现的是对于生命的感知和传达。
            　　ROOBO希望通过设计的手段，将两者的界限消弭，让产品具有容易察觉和感知的“生命力”。而这，也正符合了我们通过影视作品，在自身脑海中勾勒出的那个机器人伙伴诞生的过程。
            　　为产品注入灵魂 ROOBO借设计推动智能机器人行业变革
            　　ROOBO高级设计总监 叶菲梓 先生
